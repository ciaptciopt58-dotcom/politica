function el(tagName, options) {
  const node = document.createElement(tagName)
  if (!options) return node
  if (options.className) node.className = options.className
  if (options.text != null) node.textContent = options.text
  if (options.html != null) node.innerHTML = options.html
  if (options.attrs) {
    for (const [k, v] of Object.entries(options.attrs)) {
      node.setAttribute(k, String(v))
    }
  }
  return node
}

function sanitizeUrl(input) {
  if (input == null) return ""
  let u = String(input).trim()
  if (!u) return ""
  if ((u.startsWith("`") && u.endsWith("`")) || (u.startsWith('"') && u.endsWith('"')) || (u.startsWith("'") && u.endsWith("'"))) {
    u = u.slice(1, -1).trim()
  }
  return u
}

function isHttpUrl(url) {
  const u = sanitizeUrl(url)
  return u.startsWith("http://") || u.startsWith("https://")
}

function safeExternalLink(url) {
  return { href: sanitizeUrl(url), target: "_blank", rel: "noopener noreferrer", referrerpolicy: "no-referrer" }
}

function isPlaceholderUrl(url) {
  const u = sanitizeUrl(url)
  if (!u) return true
  const lower = u.toLowerCase()
  return u === "#" || lower === "todo"
}

function normalizeAssetPath(src) {
  const u = sanitizeUrl(src)
  if (!u) return ""
  if (u.startsWith("http://") || u.startsWith("https://")) return u
  if (u.startsWith("./") || u.startsWith("../") || u.startsWith("/")) return u
  return `./${u}`
}

function placeholderSymbolSvg(label) {
  const safe = String(label || "").slice(0, 38)
  const aria = safe.replace(/"/g, "&quot;")
  return `<svg xmlns="http://www.w3.org/2000/svg" width="54" height="54" viewBox="0 0 54 54" role="img" aria-label="${aria}"><rect x="1" y="1" width="52" height="52" rx="14" fill="#ffffff" stroke="#e2e8f0"/><path d="M18 14h6v26h-6V14zm12 0h6v26h-6V14z" fill="#1d4ed8"/><path d="M27 18c6 2 10 7 10 12 0 6-5 11-10 12" fill="none" stroke="#16a34a" stroke-width="3" stroke-linecap="round"/><path d="M31 30c0-3 3-6 6-7" fill="none" stroke="#ef4444" stroke-width="3" stroke-linecap="round"/><text x="27" y="48" text-anchor="middle" font-family="ui-sans-serif, system-ui" font-size="9.5" fill="#334155">${safe
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")}</text></svg>`
}

function renderListSymbol(list) {
  const isFeatured = sanitizeUrl(list && list.logoSrc ? list.logoSrc : "").endsWith("10.webp")
  const wrap = el("div", { className: isFeatured ? "list-symbol logo--featured" : "list-symbol" })
  const alt = String((list && list.logoAlt) || `Simbolo ${list && list.name ? list.name : "lista"}`)
  const src = normalizeAssetPath(list && list.logoSrc ? list.logoSrc : "")

  if (!src || isPlaceholderUrl(src)) {
    wrap.innerHTML = placeholderSymbolSvg(alt)
    return wrap
  }

  const img = el("img", {
    className: "list-symbol-img",
    attrs: { src, alt, width: "54", height: "54", decoding: "async", loading: "lazy" },
  })
  img.addEventListener("error", () => {
    wrap.innerHTML = placeholderSymbolSvg(alt)
  })
  wrap.appendChild(img)
  return wrap
}



function renderHero(data) {
  const kicker = document.getElementById("hero-kicker")
  const title = document.getElementById("hero-title")
  const mayor = document.getElementById("candidateName")
  const t = String(data && data.electionTitle ? data.electionTitle : "").trim()
  const match = t.match(/^(Elezioni\s+Amministrative)\s*(.*)$/i)
  const kickerText = match ? match[1].toUpperCase() : "ELEZIONI AMMINISTRATIVE"
  const mainText = match && match[2] ? match[2].trim() : t
  if (kicker) kicker.textContent = kickerText
  if (title) title.textContent = mainText || t
  if (mayor) mayor.textContent = data.candidateMayor
}

function renderCvCasellarioControl(list) {
  const label = "C.V./Casellario giudiziale"
  const url = sanitizeUrl(list && (list.documentUrl || list.cvcasellarioUrl) ? list.documentUrl || list.cvcasellarioUrl : "")
  if (!url || isPlaceholderUrl(url)) {
    const s = el("span", { className: "btn btn-outline btn-doc btn-disabled", text: label })
    s.setAttribute("aria-disabled", "true")
    return s
  }

  const a = el("a", { className: "btn btn-outline btn-doc", text: label })
  if (isHttpUrl(url)) {
    const link = safeExternalLink(url)
    a.setAttribute("href", link.href)
    a.setAttribute("target", link.target)
    a.setAttribute("rel", link.rel)
    a.setAttribute("referrerpolicy", link.referrerpolicy)
  } else {
    a.setAttribute("href", url)
  }
  return a
}

function renderTopNav(data) {
  const root = document.getElementById("topNavList")
  if (!root) return
  root.innerHTML = ""

  const addItem = (href, label) => {
    const li = el("li")
    const a = el("a", { text: label })
    a.setAttribute("href", href)
    li.appendChild(a)
    root.appendChild(li)
  }

  addItem("#home", "Home")
  addItem("#liste", "Liste")
}

function renderCandidatesGrid(list) {
  const candidates = Array.isArray(list.candidates) ? list.candidates : []
  const grid = el("ol", {
    className: "candidate-grid",
    attrs: { role: "list", "aria-label": `Candidati ${list.name}` },
  })

  for (const c of candidates) {
    const item = el("li", { className: "candidate-item" })
    item.appendChild(el("p", { className: "candidate-name", text: c.name }))
    grid.appendChild(item)
  }

  return grid
}

function renderListCard(list) {
  const card = el("article", { className: "summary-card", attrs: { "aria-label": `Lista ${list.name}` } })
  if (list.slug) card.id = list.slug

  const head = el("div", { className: "summary-head" })
  head.appendChild(renderListSymbol(list))

  const titleWrap = el("div", { className: "list-title" })
  titleWrap.appendChild(el("h3", { className: "h3", text: list.name }))
  if (list.politicalLabel) {
    titleWrap.appendChild(el("div", { className: "political-label", text: list.politicalLabel }))
  }
  head.appendChild(titleWrap)
  card.appendChild(head)

  const body = el("div", { className: "summary-body" })
  body.appendChild(el("p", { className: "muted", text: `Lista collegata alla candidatura di ${list.coalition}` }))

  const actions = el("div", { className: "card-actions" })

  const primaryActions = el("div", { className: "primary-actions" })

  const panelId = `${list.slug}-candidati`
  const toggleId = `${list.slug}-toggle`
  const toggleBtn = el("button", {
    className: "btn btn-primary",
    text: "Elenco candidati",
    attrs: { type: "button", id: toggleId, "aria-expanded": "false", "aria-controls": panelId },
  })
  primaryActions.appendChild(toggleBtn)
  primaryActions.appendChild(renderCvCasellarioControl(list))
  actions.appendChild(primaryActions)

  body.appendChild(actions)

  const panel = el("section", {
    className: "list-panel",
    attrs: { id: panelId, role: "region", "aria-labelledby": toggleId },
  })
  panel.hidden = true
  panel.appendChild(renderCandidatesGrid(list))
  body.appendChild(panel)

  toggleBtn.addEventListener("click", () => {
    const willOpen = !panel.classList.contains("is-open")
    panel.classList.toggle("is-open", willOpen)
    panel.hidden = !willOpen
    toggleBtn.setAttribute("aria-expanded", String(willOpen))
  })

  card.appendChild(body)
  return card
}

function renderLists(data) {
  const root = document.getElementById("listsRoot")
  if (!root) return
  root.innerHTML = ""

  const lists = Array.isArray(data.lists) ? data.lists : []
  for (const list of lists) {
    root.appendChild(renderListCard(list))
  }
}

function normalizeData(data) {
  const lists = Array.isArray(data.lists) ? data.lists : []
  return {
    ...data,
    lists: lists.map((list) => ({
      ...list,
      documentUrl: sanitizeUrl(list && (list.documentUrl || list.cvcasellarioUrl) ? list.documentUrl || list.cvcasellarioUrl : ""),
    })),
  }
}

function setupLegalSections() {
  const privacy = document.getElementById("privacy-policy")
  const cookie = document.getElementById("cookie-policy")
  if (!privacy || !cookie) return

  const setOpen = (which) => {
    const openPrivacy = which === "privacy"
    const openCookie = which === "cookie"
    privacy.classList.toggle("is-open", openPrivacy)
    cookie.classList.toggle("is-open", openCookie)
  }

  const openFromHash = () => {
    const hash = String(window.location.hash || "")
    if (hash === "#privacy-policy") {
      setOpen("privacy")
      requestAnimationFrame(() => privacy.scrollIntoView({ block: "start" }))
      return
    }
    if (hash === "#cookie-policy") {
      setOpen("cookie")
      requestAnimationFrame(() => cookie.scrollIntoView({ block: "start" }))
      return
    }
    setOpen("none")
  }

  openFromHash()
  window.addEventListener("hashchange", openFromHash, { passive: true })
}

function init() {
  const data = normalizeData(siteData)
  renderHero(data)
  renderTopNav(data)
  renderLists(data)
  setupLegalSections()

  requestAnimationFrame(() => {
    document.documentElement.classList.add("is-ready")
  })

  const year = new Date().getFullYear()
  const footerYear = document.getElementById("footerYear")
  if (footerYear) footerYear.textContent = `© ${year} – ${data.electionTitle}`
}

document.addEventListener("DOMContentLoaded", init)
